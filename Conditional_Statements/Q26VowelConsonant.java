// Q26. Vowel or Consonant

class Q26VowelConsonant {
    public static void main(String[] args) {

        char ch = 'a';

        if(ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u') {
            System.out.println(ch + " is a vowel");
        } else {
            System.out.println(ch + " is a consonant");
        }

    }
}

/*
Expected Output:
a is a vowel
*/
