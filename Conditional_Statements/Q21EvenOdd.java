// Q21. Even or Odd

class Q21EvenOdd {
    public static void main(String[] args) {

        int number = 7;

        if(number % 2 == 0) {
            System.out.println(number + " is even");
        } else {
            System.out.println(number + " is odd");
        }

    }
}

/*
Expected Output:
7 is odd
*/
