// Q6. Variables and Data Types

class Q6VariablesDataTypes {
    public static void main(String[] args) {

        int integerValue = 10;
        float floatValue = 5.75f;
        double doubleValue = 19.99;
        char characterValue = 'A';
        boolean booleanValue = true;

        System.out.println("Integer: " + integerValue);
        System.out.println("Float: " + floatValue);
        System.out.println("Double: " + doubleValue);
        System.out.println("Character: " + characterValue);
        System.out.println("Boolean: " + booleanValue);

    }
}

/*
Expected Output:
Integer: 10
Float: 5.75
Double: 19.99
Character: A
Boolean: true
*/
