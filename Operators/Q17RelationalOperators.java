// Q17. Relational Operators

class Q17RelationalOperators {
    public static void main(String[] args) {

        int a = 10;
        int b = 5;

        System.out.println("a > b: " + (a > b));
        System.out.println("a < b: " + (a < b));
        System.out.println("a == b: " + (a == b));
        System.out.println("a != b: " + (a != b));
        System.out.println("a >= b: " + (a >= b));
        System.out.println("a <= b: " + (a <= b));

    }
}

/*
Expected Output:
a > b: true
a < b: false
a == b: false
a != b: true
a >= b: true
a <= b: false
*/
