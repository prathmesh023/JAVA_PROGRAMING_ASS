// Q20. Increment and Decrement Operators

class Q20IncrementDecrement {
    public static void main(String[] args) {

        int a = 5;

        System.out.println("a++: " + a++);
        System.out.println("++a: " + ++a);
        System.out.println("a--: " + a--);
        System.out.println("--a: " + --a);

    }
}

/*
Expected Output:
a++: 5
++a: 7
a--: 7
--a: 5
*/
