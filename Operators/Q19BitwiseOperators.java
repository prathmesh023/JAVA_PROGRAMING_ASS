// Q19. Bitwise Operators

class Q19BitwiseOperators {
    public static void main(String[] args) {

        int a = 6;
        int b = 4;

        System.out.println("a & b: " + (a & b));
        System.out.println("a | b: " + (a | b));
        System.out.println("a ^ b: " + (a ^ b));

    }
}

/*
Expected Output:
a & b: 4
a | b: 6
a ^ b: 2
*/
