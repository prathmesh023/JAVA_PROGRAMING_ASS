// Q9. Constant PI Example

class Q9ConstantPI {
    public static void main(String[] args) {

        final double PI = 3.14;
        int radius = 5;

        double area = PI * radius * radius;

        System.out.println("Area of the circle: " + area);

    }
}

/*
Expected Output:
Area of the circle: 78.5
*/
